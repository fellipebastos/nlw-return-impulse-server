export class NodemailerMailAdapter implements MailAdapter {
  async sendMail({ subject, body }: SendMailData) {
    await transport.sendMail({
      from: "Equipe Feedget <oi@feedget.com>",
      to: "Fellipe Bastos <fellipebastoswm@gmail.com>",
      subject,
      html: body,
    });
  }
}
